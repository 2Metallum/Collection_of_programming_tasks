<?php

/*
 * 10.27. Дана строка-предложение на русском языке.
        Преобразовать строку так, чтобы каждое слово
        начиналось с заглавной буквы.
*/
//почему-то у меня не работает функция ucwords(), я воспользовался помощью ИИ ;)

function foo($string) {
    $words = explode(" ", $string);

// Преобразуем первую букву каждого слова в верхний регистр
    foreach ($words as &$word) {
        $word = mb_convert_case($word, MB_CASE_TITLE, "UTF-8");
    }

// Объединяем слова обратно в строку
    $string = implode(" ", $words);

    echo $string;
}

foo("дана строка-предложение на русском языке. Вот!");