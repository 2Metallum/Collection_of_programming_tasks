<?php

/*
 * 13.8. Описать процедуру SplitStr(S,W,N), которая
        формирует по данной строке S набор слов W,
        входящих в S (W — выходной строковый массив; N —
        его размер; предполагается, что N не будет
        превышать 10). Под словом понимается набор
        символов, не содержащий пробелов и ограниченный
        пробелами или началом/концом строки. Используя эту
        функцию, вывести количество слов N, содержащихся в
        данной строке S, и сами эти слова.
*/

function SplitStr($S, &$W, &$N) {
    $words = preg_split('/\s+/', trim($S));
    $N = min(count($words), 10);
    for ($i = 0; $i < $N; $i++) {
        $W[$i] = $words[$i];
    }
}

$S = "Это пример строки для тестирования функции SplitStr";
$N = 0;
$W = array();
SplitStr($S, $W, $N);
echo "Количество слов: " . $N . "\n";
echo "Слова: ";
for ($i = 0; $i < $N; $i++) {
    echo $W[$i] . " ";
}